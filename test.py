
u = [16000]  # начальное значение u_1
c = 190000 / 101  # значение константы c

# Проверка рекуррентного соотношения
for n in range(1, 100):  # проверяем для первых 100 месяцев
    # Вычисляем u_{n+1} с помощью вашего решения и рекуррентного соотношения
    u_n_plus_1_from_solution = c * 1.01**(n+1) - 35000
    u_n_plus_1_from_recurrence = 1.01 * u[-1] - 350

    # Сравниваем два значения u_{n+1}
    if abs(u_n_plus_1_from_solution - u_n_plus_1_from_recurrence) > 1e-6:  # разница больше, чем допустимая погрешность
        print(f"Решение неверно для n = {n}")
        break

    # Добавляем u_{n+1} в список
    u.append(u_n_plus_1_from_recurrence)

    # Проверяем, выплачен ли долг
    if u[-1] <= 0:
        print(f"Долг будет выплачен через {n+1} месяцев")
        break